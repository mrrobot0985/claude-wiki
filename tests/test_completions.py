def test_bash_completion_contains_registry_subcommands() -> None:
    """The bash script completes the nested registry subcommands."""
    script = _read_script("claude-wiki.bash")
    for sub in ("list", "show", "remove", "clean"):
        assert sub in script, f"bash completion missing registry subcommand: {sub}"


def test_generator_script_runs_without_error() -> None:
    """The standalone generator script exits 0 and writes the three files."""
    result = subprocess.run(
        [sys.executable, str(GENERATOR_SCRIPT), "--check"],
        cwd=str(REPO_ROOT),
        capture_output=True,
        text=True,
    )
    assert result.returncode == 0, result.stderr


def test_completion_scripts_do_not_drift(tmp_path: Path) -> None:
    """Regenerating the scripts must match the committed files exactly."""
    generate_all(tmp_path)

    for name in COMPLETION_NAMES:
        expected = COMPLETIONS_DIR / name
        generated = tmp_path / name
        assert generated.exists(), f"Generator did not produce {name}"
        assert generated.read_bytes() == expected.read_bytes(), (
            f"Completion script drifted: {name}. "
            f"Run `uv run python scripts/generate_completions.py` to regenerate."
        )
